# Define the list of numbers
numbers_list = [1, 2, 3, 4, 5]

# Initialize a variable to store the sum
total_sum = 0

# Use a for loop to iterate through the list and add each number to the total_sum
for number in numbers_list:
    total_sum += number

# Print the result
print("The sum of all values in the list is:", total_sum)
