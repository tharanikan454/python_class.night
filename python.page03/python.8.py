#Integer
my_var = 10
print(type(my_var))

#Float
my_var = 10.5
print(type(my_var))

#String
my_var = "Hello, World!"
print(type(my_var))

#Boolean
my_var = True
print(type(my_var))

#List
my_var = [1, 2, 3, 4]
print(type(my_var))



#Changing the Type of a Variable
my_var = 42
print(type(my_var))

my_var = "Now I'm a string!"
print(type(my_var))


#Typecasting (Explicit Type Conversion)


#Integer to Float
my_var = 10
my_var = float(my_var)
print(type(my_var))
print(my_var)


#String to Integer
my_var = "123"
my_var = int(my_var)
print(type(my_var))
print(my_var)


#Float to String
my_var = 10.5
my_var = str(my_var)
print(type(my_var))
print(my_var)


#List to Tuple
my_list = [1, 2, 3]
my_tuple = tuple(my_list)
print(type(my_tuple))
print(my_tuple)

#String to List
my_var = "hello"
my_var = list(my_var)
print(type(my_var))
print(my_var)
