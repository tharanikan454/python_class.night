#Mutable Variables

#List
my_list = [1, 2, 3]
my_list.append(4)
print(my_list)

#Dictionary
my_dict = {'a': 1, 'b': 2}
my_dict['c'] = 3
print(my_dict)

#---------------------------------------------------------------------------------------------------------------------

#Immutable Variables

#String
my_string = "tharani"
new_string = my_string + " kan"
print(my_string)
print(new_string)

#Tuple
my_tuple = (1, 2, 3)
new_tuple = my_tuple + (4,)
print(my_tuple)
print(new_tuple)


